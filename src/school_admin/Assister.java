package school_admin;


public class Assister {

  private long matricule;
  private long code;
  private double noteDe;
  private double noteTp;
  private double notePjr;


  public long getMatricule() {
    return matricule;
  }

  public void setMatricule(long matricule) {
    this.matricule = matricule;
  }


  public long getCode() {
    return code;
  }

  public void setCode(long code) {
    this.code = code;
  }


  public double getNoteDe() {
    return noteDe;
  }

  public void setNoteDe(double noteDe) {
    this.noteDe = noteDe;
  }


  public double getNoteTp() {
    return noteTp;
  }

  public void setNoteTp(double noteTp) {
    this.noteTp = noteTp;
  }


  public double getNotePjr() {
    return notePjr;
  }

  public void setNotePjr(double notePjr) {
    this.notePjr = notePjr;
  }

}
